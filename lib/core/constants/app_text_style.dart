    Color textColor = Colors.black,
    double fontSize = 10.0,
    FontWeight fontWeight = FontWeight.w400,
    TextDecoration decoration = TextDecoration.none,
    double? height,
    double? letterSpacing,
  }) {
    return GoogleFonts.raleway(
